CLEAR SCREEN
SET SERVEROUTPUT ON

CREATE OR REPLACE FUNCTION ISARMSTRONG(N IN NUMBER) RETURN BOOLEAN IS 
	X NUMBER:=N;
	C NUMBER:=0;
	D NUMBER:=0;
	S NUMBER:=0;
	

BEGIN
	WHILE X<>0 LOOP
		C:=C+1;
		X:= TRUNC(X/10);
	END LOOP;
	X := N;
	WHILE X<>0 LOOP
		D := MOD(X,10);
		S:= S+(D**C);
		X:= TRUNC(X/10);
	END LOOP;
	IF S = N THEN
		RETURN TRUE;
	ELSE
		RETURN FALSE;
	END IF;
	
END;
/
